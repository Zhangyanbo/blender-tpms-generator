def build_unit_cell(tpms_type='GYROID', cell_size=1.0, res=2,
                    solver_resolution=44, quadrature_order=200):
    """Build one cubic unit cell of an exact TPMS as an all-quad mesh.

    Parameters
    ----------
    tpms_type : str   -- key into SURFACES ('GYROID', 'SCHWARZ_P',
                         'SCHWARZ_D').
    cell_size : float -- world edge length of the cubic translational cell.
    res       : int   -- quads per macro-patch edge. G/P/D have
                         48/24/96 * res^2 genuine macro quads.

    Returns
    -------
    verts   : (V, 3) float64 -- welded vertices, cell spans ~[0, cell_size]^3
    quads   : (Q, 4) int32   -- quad faces, globally consistent orientation
    normals : (V, 3) float64 -- smooth unit normals
    """
    if tpms_type == 'GYROID':
        return gyroid_macro.build_unit_cell(
            cell_size=cell_size,
            quad_subdivisions=res,
            solver_resolution=solver_resolution,
            quadrature_order=quadrature_order,
        )

    surf = SURFACES[tpms_type]
    if tpms_type in {'SCHWARZ_P', 'SCHWARZ_D'}:
        reference_patch, _ = _fundamental_patch(surf, 2)
        return bonnet_macro.build_unit_cell(
            surface_name=tpms_type,
            surface=surf,
            reference_patch=reference_patch,
            cell_size=cell_size,
            quad_subdivisions=res,
            solver_resolution=solver_resolution,
            quadrature_order=quadrature_order,
        )

    res = max(2, int(res))
    patch, pnorm = _fundamental_patch(surf, res)
    npts = (res + 1) * (res + 1)

    idx = np.arange(npts).reshape(res + 1, res + 1)
    q0 = np.stack([idx[:-1, :-1].ravel(), idx[1:, :-1].ravel(),
                   idx[1:, 1:].ravel(), idx[:-1, 1:].ravel()], axis=1)

    pflat = patch.reshape(-1, 3)
    nflat = pnorm.reshape(-1, 3)
    nops = len(surf.ops)
    all_v = np.empty((nops * npts, 3))
    all_n = np.empty((nops * npts, 3))
    all_q = np.empty((nops * res * res, 4), dtype=np.int64)

    q0r = q0[:, ::-1]                        # reversed winding
    for k, row in enumerate(surf.ops):
        M = np.array(row[:9], dtype=float).reshape(3, 3)
        t = np.array(row[9:12], dtype=float) / 16.0
        sign = float(row[12])                # +1 keeps sides, -1 swaps
        p = pflat @ M.T + t
        t -= np.floor(p.mean(axis=0))        # keep the copy near [0, 1)^3
        det = float(np.linalg.det(M))
        all_v[k * npts:(k + 1) * npts] = pflat @ M.T + t
        all_n[k * npts:(k + 1) * npts] = nflat @ M.T * (sign * det)
        all_q[k * res * res:(k + 1) * res * res] =             (q0 if sign > 0 else q0r) + k * npts

    # weld: vertices from different patches on shared boundary curves agree
    # to ~1e-9, far below the smallest quad size (~0.1 / res).
    tol = 1e-6
    keys = np.round(all_v / tol).astype(np.int64)
    _, first, inverse = np.unique(keys, axis=0, return_index=True,
                                  return_inverse=True)
    verts = all_v[first] * cell_size
    normals = all_n[first]
    quads = inverse[all_q].astype(np.int32)
    return verts, quads, normals
