"""Exact parametric TPMS (Gyroid, Schwarz P, Schwarz D) via the
Enneper-Weierstrass representation.

References
----------
* Gyroid:    P.J.F. Gandy, J. Klinowski, "Exact computation of the triply
  periodic G ('Gyroid') minimal surface", Chem. Phys. Lett. 321 (2000)
  363-371.  doi:10.1016/S0009-2614(00)00373-0
* Schwarz P: P.J.F. Gandy, J. Klinowski, "Exact computation of the triply
  periodic Schwarz P minimal surface", Chem. Phys. Lett. 322 (2000)
  579-586.  doi:10.1016/S0009-2614(00)00453-X
* Schwarz D: P.J.F. Gandy, D. Cvijovic, A.L. Mackay, J. Klinowski, "Exact
  computation of the triply periodic D ('diamond') minimal surface",
  Chem. Phys. Lett. 314 (1999) 543-551.  doi:10.1016/S0009-2614(99)01000-3

All three surfaces are Bonnet associates sharing the Weierstrass function

    R(tau) = 1 / sqrt(tau^8 - 14 tau^4 + 1)

with Bonnet angles theta_D = 0, theta_G = arccot(K'/K) = 38.0147740 deg,
theta_P = 90 deg, where K = K(m=1/4), K' = K(m=3/4) are complete elliptic
integrals (computed here by the AGM):

    r(omega) = Re[ e^{i theta} Int_0^omega (1-tau^2, i(1+tau^2), 2tau)
                                            R(tau) dtau ].

Every surface's fundamental curved triangles are paired across their circular
edges into genuine macro-quadrilateral patches. A shared cached cotangent-
harmonic square map removes the hidden diagonal's parameter kink. The Coons
evaluator below is retained only to resolve the published space-group copies.

The isometries assembling the fundamental patch into one cubic unit cell
were *derived numerically*, not copied from the papers (whose assembly
tables rest on internally inconsistent frame descriptions): the surface was
analytically continued across the three patch boundary curves, the
side-pairing isometries were extracted by Procrustes fits (residual ~1e-9),
the space group was generated by composition, and the cubic lattice was
read off from its pure translations. This reproduces the expected
crystallography exactly:

    Gyroid:    Ia-3d (No. 230),  96 triangles -> 48 macro quads, bcc
    Schwarz P: Im-3m (No. 229),  48 triangles -> 24 macro quads, bcc
    Schwarz D: Fd-3m (No. 227), 192 triangles -> 96 macro quads, fcc

The cubic lattice constants come out as exact elliptic-integral
expressions (verified against the integration to ~1e-9):

    L_P = K',    L_D = 2K,    L_G = 2KK' / sqrt(K^2 + K'^2)

(in kappa = 1 integration units). Rotation parts of all assembly
operations are signed permutation matrices; translations are multiples of
L/16; each operation carries an orientation sign (whether it swaps the two
sides of the surface -- for P and D this is NOT the determinant: their
symmetry groups contain in-surface 2-fold axes, proper yet side-swapping,
and surface-orthogonal mirrors, improper yet side-preserving). Every emitted mesh was verified: 100% quads, zero degenerate faces,
globally consistent orientation, all vertices on the exact minimal surface,
and exact periodicity -- on the 3-torus every edge borders exactly two
quads (no gaps, no overlaps), so Array-modifier tiles weld seamlessly.

Each cell is aligned with the standard level-set conventions (coordinates
in units of 2 pi / cell):

    Gyroid:    sin x cos y + sin y cos z + sin z cos x     (mean |F| ~ 0.007)
    Schwarz P: cos x + cos y + cos z                       (mean |F| ~ 0.045)
    Schwarz D: sin x sin y sin z + sin x cos y cos z
               + cos x sin y cos z + cos x cos y sin z     (mean |F| ~ 0.002)

This module is pure numpy (no bpy) and unit-tested outside Blender.
"""

